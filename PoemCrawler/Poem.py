# 寻找诗文正文的第一页链接
def _get_poem_link(_soup):
    _link = _soup.p.a['href']
    return _link


# 寻找下一页链接
def _get_poem_next_page(_soup):
    _nxt_page = _soup.find('a', style='width:60px;')
    if _nxt_page is not None:
        return _nxt_page['href']
    else:
        return None
    pass


