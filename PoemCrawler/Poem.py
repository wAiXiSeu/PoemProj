def _get_poem_content(_soup):
    _sons = _soup.find_all('div', class_='sons')
    _poem_info = {
        'id': '',
        'title': '',
        'dynasty': '',
        'author': '',
        'content': ''
    }
    if _sons is not None:
        for s in _sons:
            if s.find('a', target='_blank') is not None:
                _title = s.find('a', target='_blank').string
                _poem_info['title'] = _title
            if s.find('p', class_='source') is not None:
                _temp_arr = s.find('p', class_='source').text.split('：')
                if len(_temp_arr) > 0:
                    _dynasty = s.find('p', class_='source').text.split('：')[0]
                    _author = s.find('p', class_='source').text.split('：')[1]
                    _poem_info['dynasty'] = _dynasty
                    _poem_info['author'] = _author
            if s.find('div', class_='contson') is not None:
                _content = s.find('div', class_='contson').text.strip()
                _poem_id = re.sub('\D+', '', s.find('div', class_='contson')['id'])
                _poem_info['content'] = _content
                _poem_info['id'] = _poem_id
    return _poem_info
    pass


def get_poem(_base_url, _author_links):
    author_soups = get_soups(_author_links)
    poem_links = eventlet.GreenPool().imap(_get_poem_link, author_soups)
    poem_urls = []
    for l in poem_links:
        poem_urls.append(_base_url + l)
    poem_soups = [s for s in get_soups(poem_urls)]
    # print(poem_soups)
    pool = ThreadPool(multiprocessing.cpu_count())
    poem_contents = []
    soup_per_process = int(math.ceil((len(poem_soups)/multiprocessing.cpu_count())))
    for i in range(multiprocessing.cpu_count()):
        contents = pool.map_async(_get_poem_content, poem_soups[i*soup_per_process:(i+1)*soup_per_process])
        if contents is not None:
            poem_contents.append(contents)
    pool.close()
    pool.join()
    return poem_contents
    pass


